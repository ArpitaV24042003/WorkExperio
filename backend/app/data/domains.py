def get_projects_for_domain(domain: str) -> list:
    """Get all project templates for a specific domain"""
    return PROJECT_TEMPLATES.get(domain, [])

def get_all_domains() -> list:
    """Get all available domains"""
    return DOMAINS

def get_project_by_domain_and_index(domain: str, index: int) -> str:
    """Get a specific project by domain and index"""
    projects = get_projects_for_domain(domain)
    if 0 <= index < len(projects):
        return projects[index]
    return None

